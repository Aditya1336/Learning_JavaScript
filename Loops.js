//For loop
console.log("For loop ->")
for(let i=0;i<5;i++){
    console.log(i);
}


//do...while loop
console.log("do while loop ->")
{
    let i=0;
    do{
        i+=1;
        console.log(i);
    }while(i<5);
}


//While Loops 
console.log("While loop ->")
{
    let i = 0;
    let n = 3;
    while(i<3){
        console.log(i);
        n+=i;
        i+=1;
    }
    console.log(n)
}

